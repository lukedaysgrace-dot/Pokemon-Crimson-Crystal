; Battle anim OAM framesets.
; 3-byte entries (word OAM set id + duration byte) so more than 255 OAM sets
; can be used (format ported from polishedcrystal).

oamframe: MACRO
	dw \1
x = \2
if _NARG > 2
rept _NARG + -2
x = x | (1 << (\3 + 1))
	shift
endr
endc
	db x
ENDM

oamdelanim: MACRO
; Terminal: the engine deletes the object before reading a duration byte,
; so no third byte is emitted.
	dw OAMDELANIM_COMMAND
ENDM

oamdowait: MACRO
	dw OAMDOWAIT_COMMAND
	db \1 ; frames
ENDM

oamrestart: MACRO
	dw OAMRESTART_COMMAND
ENDM

oamendanim: MACRO
	dw OAMENDANIM_COMMAND
ENDM

